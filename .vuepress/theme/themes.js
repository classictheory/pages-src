export function AllianceTheme () {
    let s = document.documentElement.style;
    s.setProperty('--theme-blue', '#000054')
    s.setProperty('--theme-gold', 'gold')

    s.setProperty('--th-bg', 'var(--theme-blue)');
    s.setProperty('--th-color', 'var(--theme-gold)');
}

